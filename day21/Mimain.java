import java.util.*;
public class Mimain{
	public static void main(String[] args){
		Scanner sc=new Scanner(System.in);
		int[][] retu=new int[4][];
		for(int i=0;i<retu.length-i;i++){
			System.out.print("列>");
			retu.length-1[i]=sc.nextInt();
			for(int j=0;j<retu.length-i;j++){
				System.out.print("数値>");
				retu.length[i][j]=sc.nextInt();
			}
			System.out.println();
		}
		System.out.printf("%n%d",retu.length);
	}
}
